package com.class9;

public class Odometer {
	public static void main(String args[]) {
       
		for(int f=0; f<=9; f++) {
            for(int s=0; s<=9; s++) {
                for(int t=0; t<=9; t++) {
                    for(int fo=0; fo<=9;fo++) {
                        System.out.println(f+""+s+""+t+""+fo);
                    }
                }
                
            }
        }
		
		
		
		
		
    }
}
